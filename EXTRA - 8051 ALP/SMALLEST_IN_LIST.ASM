;8051 TO FIND THE SMALLEST NO. IN THE ARRAY

START:
MOV R0, #00

;LOAD DATA INTO MEMORY
MOV 20H, #34H
MOV 21H, #0ADH
MOV 22H, #0CDH
MOV 23H, #43H
MOV 24H, #69H

;LOAD THE LENGTH
MOV 01, #05H

;FIND THE SMALLEST NUMBER
MOV R0, #20H		;LOAD THE BASE ADDRESS
CLR A				;CLEAR ACCUMULATOR

MOV A, #0FFH		;LARGEST NUMBER POSSIBLE

BACK:
MOV B, @R0			;MOVE R0'S VALUE TO B
CJNE A, B, NEXT	;IF A != B, JUMP TO NEXT

NEXT:
JC SKIP				;IF A < B, CARRY BIT = 1
MOV A, B			;A IS THE CURRENT SMALLEST VALUE

SKIP:
INC R0				;GOTO NEXT ELEMENT
DJNZ R1, BACK		;IF R0 != R1, GO BACK


MOV 30H, A			;MOVE SMALLEST ELEMENT TO 30H

HERE:
SJMP HERE			;HALT
