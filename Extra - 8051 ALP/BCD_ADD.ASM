;PROGRAM TO ADD 2 BCD VALUES

;01 + 11 = (20 21)

;RESET R0
MOV R0, #00

;INITIALIZE VALUES TO ADD
MOV 01H, #99H
MOV 11H, #99H

;CLEAR RESULT
MOV 20H, #00H
MOV 21H, #00H

;ADDING TWO VALS
ADD A, 01H
ADD A, 11H
DA A
JNC SKIP
INC 20H
SKIP:
MOV 21H, A

;EXIT CODE
HALT: 
SJMP HALT