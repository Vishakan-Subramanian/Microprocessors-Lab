;PROG TO ADD TWO 16 BIT NUMBERS
;(11 12) + (21 22) = (30 31 32)

;CLEAR R0
MOV R0, #00

;INITIALIZE REGS WITH VALUES
MOV 11H, #0DBH
MOV 12H, #0CCH

MOV 21H, #045H
MOV 22H, #0FFH

;INITIALIZE SUM AS ZERO
MOV 30H, #00
MOV 31H, #00
MOV 32H, #00

;ADDING LOWER NIBBLES
ADD A, 12H
ADD A, 22H
MOV 32H, A
CLR A

;ADDING HIGHER NIBBLES
ADDC A, 11H
ADDC A, 21H
MOV 31H, A

;ADDING CARRY
JNC HALT
INC 30H

HALT:
SJMP HALT
