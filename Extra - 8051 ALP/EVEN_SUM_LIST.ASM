;SUM OF EVEN NUMBERS IN A LIST


START:
MOV R0, #10H		;START POSITION OF LIST
MOV R1, #05H		;COUNTER
MOV 30H, #00H		;CARRY
MOV 31H, #00H		;SUM

;LOAD VALUES INTO THE INTERNAL RAM
MOV 10H, #02H
MOV 11H, #03H
MOV 12H, #02H
MOV 13H, #03H
MOV 14H, #02H

;ADD THE EVEN NUMBERS ALONE

CLR A			;CLEAR ACCUMULATOR

BACK:
	MOV A, @R0
	RRC A		
	JC SKIP		;IF CARRY = 1 => NUMBER IS ODD
	RLC A		;GET BACK THE NUMBER
	ADD A, 31H	
	MOV 31H, A	;PUT THE SUM IN 31H
	JNC SKIP	;IF NO CARRY, SKIP
	INC 30H		;IF CARRY, ADD VALUE IN 30H

SKIP:
	INC R0		;GOTO NEXT ELEMENT
	DJNZ R1, BACK	;IF R0 != R1, GO BACK
	
