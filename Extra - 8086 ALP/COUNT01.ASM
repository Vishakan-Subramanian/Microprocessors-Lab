;8086 ALP TO COUNT THE NUMBER OF ZEROES AND ONES IN AN 8-BIT NUMBER

ASSUME CS: CODE, DS: DATA

DATA SEGMENT
    NUM     DB  07H
    ZEROES  DB  ?
    ONES    DB  ?
DATA ENDS
 
CODE SEGMENT
    ORG 0100H

START:
    MOV AX, DATA
    MOV DS, AX

    MOV AH, 00H
    MOV CH, 00H
    MOV AL, NUM
    
    MOV BL, 00H         ;TO COUNT THE NUMBER OF ZEROES
    MOV CL, 08H         ;NO. OF DIGITS
    MOV DL, 00H         ;TO COUNT THE NUMBER OF ONES
 
LOOPER:
	SHL AL, 1           ;SHIFT LEFT BY 1, IF CARRY IS 1, THEN THE LSB DIGIT WAS 1.
	JC ONE              ;GO TO ONE
	INC BL              ;ELSE, INCREMENT ZEROES COUNT
	JMP SKIP            ;NEXT ITERATION
 
ONE:
	INC DL              ;INCREMENT ONES COUNT
 
SKIP:
	DEC CL              ;DECREMENT COUNTER
	JNZ LOOPER          ;IF CL != 0, CONTINUE LOOPING
 
    MOV ZEROES, BL      ;MOVE THE OUTPUTS
    MOV ONES, DL

    MOV AH, 4CH         ;TERMINATE THE PROGRAM
    INT 21H

CODE ENDS

END START
