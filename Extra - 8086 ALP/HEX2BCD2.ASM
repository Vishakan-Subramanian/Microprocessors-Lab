;8086 ALP TO CONVERT A NUMBER FROM HEX TO BCD AND DISPLAY THE ASCII VALUE OF THE UNPACKED BCD NUMBER

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
    HEX_NUM DB 0FFH
    ASCII1 DB 00H
    ASCII2 DB 00H
    ASCII3 DB 00H
DATA ENDS

CODE SEGMENT
    ORG 0100H

START:
    MOV AX, DATA
    MOV DS, AX
    MOV AL, HEX_NUM
    MOV AH, 00H
    MOV BL, 64H
    DIV BL              ;DIVIDE BY 100
    MOV CL, AL          ;MSB
    MOV AL, AH          ;REMAINDER = NEXT DIVIDEND
    MOV AH, 00H
    MOV BL, 0AH
    DIV BL              ;DIVIDE BY 10
    MOV CH, AL          ;2ND MSB
    MOV DL, AH          ;LSB
    MOV BL, 10H

    MOV AL, CL
    CLC
    ADC AL, 30H         ;MSB TO ASCII
    MOV ASCII1, AL

    MOV AL, CH
    CLC
    ADC AL, 30H         ;2ND MSB TO ASCII
    MOV ASCII2, AL

    MOV AL, DL
    CLC
    ADC AL, 30H
    MOV ASCII3, AL      ;LSB TO ASCII

    MOV AH, 4CH
    INT 21H

CODE ENDS
END START
