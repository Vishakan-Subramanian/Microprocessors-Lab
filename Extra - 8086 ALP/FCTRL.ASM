;8086 ALP TO FIND THE FACTORIAL OF A GIVEN NUMBER

ASSUME CS: CODE, DS: DATA

DATA SEGMENT
    NUM DB 05H
    RESULT DB ?
DATA ENDS

CODE SEGMENT
    ORG 0100H

START:
    MOV AX, DATA
    MOV DS, AX
    MOV AL, NUM

HERE:
    DEC NUM         ;DECREMENT NUM & MULTIPLY WITH AL
    MUL NUM
    MOV CL, NUM     
    CMP CL, 01      ;IF NUM = 1, THEN STOP MULTIPLYING
    JNZ HERE        ;ELSE LOOP
    MOV RESULT, AL  ;STORE THE VALUE IN AL AS THE RESULT

    MOV AH, 4CH
    INT 21H

CODE ENDS
END START